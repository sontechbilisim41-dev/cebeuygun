export const getRandomImage = (category: keyof typeof PRODUCT_IMAGES): string => {
  const images = PRODUCT_IMAGES[category];
  if (!images || images.length === 0) {
    return PRODUCT_IMAGES.clothing[0]; // Fallback
  }
  return images[Math.floor(Math.random() * images.length)];
};

export const getRandomUserAvatar = (): string => {
  return USER_AVATARS[Math.floor(Math.random() * USER_AVATARS.length)];
};

export const getRandomVendorImage = (): string => {
  return VENDOR_IMAGES[Math.floor(Math.random() * VENDOR_IMAGES.length)];
};